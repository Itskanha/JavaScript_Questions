// 10. **Generate a random float between 0 and 1:**  
//     ```javascript
//     console.log(getRandomFloat()); // Output: A random float between 0 and 1
//     ```

function getRandomFloat() {
    // Generate and return a random float between 0 and 1
    return Math.random(); // Math.random() returns a float in the range [0, 1)
    
}

// Test the function by logging the output
console.log(getRandomFloat()); // Output: A random float between 0 and 1
